// Aprendendo mais sobre os Arrays
const array = [21,6,84,24,86,11]
console.log(array)
//Arrays -> método
//Inserir dados
array.push(5) // Adiciona uma informação no final do array
console.log(array)
array.unshift(5) // Adiciona uma informação no início do Array
console.log(array)
//Remover dados
array.pop() // Remove a informação do final do Array
console.log(array)
array.shift()
console.log(array) // Remove a informação do início do Array

