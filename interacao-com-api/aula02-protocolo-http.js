//Metodos do Protocolo HTTP:

//GET: Solicita dados de um recurso especifico.
//POST: Envia dados para serem processados a um recurso específico.
//PUT: Atualiza um recurso existente.
//PATH: Atualiza parte de um recurso existente.
//DELETE: Remove um recurso.

//1xx: Informativo.
//2xx: Sucesso.
//3xx: Redirecionamento.
//4xx: Erro do Cliente.
//5xx: Erro do Servidor.

// INTERAGINDO COM API //

//Endpoint:
//Um recurso específico na API, geralmente representado por uma URL.
//Exemplo: `https://api.exemplo.com/usuarios`