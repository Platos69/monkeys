const array = [21, 6, 84, 24, 86, 11];
console.log(array)
// Arrays -> métodos comuns
// Inserir dados
array.push(5) //adiciona ao fim
console.log(array)
array.unshift(5) //adiciona o valor ao começo do array
console.log(array)
// Remover dados
array.pop() //remove os dados no final
console.log(array)
array.shift() //remove os dados no inicio
console.log(array)